package day16;

public class LoopIntro {
    public static void main(String[] args) {
        int count =1;

        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        ++count;
        System.out.println("Hello World");
        System.out.println("Count is "+count);

        System.out.println("----------------------");

        int cnt = 1;
        while(cnt<=5){
            System.out.println("Hello");
            System.out.println("Cnt value is " +cnt);
            ++cnt;
        }
        int x = 100;
        while(x<105){
            System.out.println("Hello World");
            ++x;

        }
    }
    }

