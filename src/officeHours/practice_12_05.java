package officeHours;

public class practice_12_05 {
    public static void main(String[] args) {

        //  -------   FOR LOOP ------
        for(int i =10; i <=20;i++){

            System.out.println(i);

        }
        System.out.println("===================");
        // ------- while loop --------
        int j=10;
        while(j<=20){
            System.out.println("number: "+j);
            j++;
        }
        System.out.println("==================");
        // ------- DO While Loop -------

        int z = 0;
        do{
            System.out.println("COunt: " + z);
            z++;
        } while(z<=20);

    }
}
